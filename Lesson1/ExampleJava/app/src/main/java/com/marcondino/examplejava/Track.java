package com.marcondino.examplejava;

public class Track {
    private String name;
    private String instructor;

    Track(String name, String instructor){
        this.name = name;
        this.instructor = instructor;
    }

    public String getName() {
        return name;
    }

    public String getInstructor() {
        return name;
    }

    public String setName() {
        return name;
    }

    public String setInstructor() {
        return name;
    }
}
